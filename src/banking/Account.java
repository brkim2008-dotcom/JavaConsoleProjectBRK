package banking;

/* 기본 계좌 클래스
 - Account 클래스 멤버 변수선언
 - 생성자 선언                 */

public class Account {
  private String accountNumber;
  private String name;
  private int balance;
  
  public Account(String accountNumber, String name, int balance) {
	  this.accountNumber = accountNumber;
	  this.name = name;
	  this.balance = balance;
  }
}


  
  
  
  
